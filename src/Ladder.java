public class Ladder {
    public static void main(String[] args) {
        int x;

        for(x=0;x<6;x++){
            if(x==1)
                System.out.println("x egal cu unu");
            else if (x==2) System.out.println("x egal cu doi");
            else if (x==3) System.out.println("x egal cu trei");
            else if (x==4) System.out.println("x egal cu patru");
            else System.out.println("in afara de 1-4");
            }
        }
    }

